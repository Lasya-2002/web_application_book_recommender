# Authentication forms
def login_form():
    with st.form("Login"):
        st.write("### User Login")
        username = st.text_input("Username")
        password = st.text_input("Password", type="password")
        submit = st.form_submit_button("Login")
        
        if submit:
            response = requests.post(
                'http://localhost:5000/login',
                json={'username': username, 'password': password}
            )
            if response.status_code == 200:
                state.logged_in = True
                state.session_id = response.json()['session_id']
                state.username = username
                state.show_login = False
                st.rerun()
            else:
                st.error("Invalid credentials")

def signup_form():
    with st.form("Sign Up"):
        st.write("### Create New Account")
        username = st.text_input("Username")
        password = st.text_input("Password", type="password")
        st.markdown(
            "#### Disclaimer ğŸ“¢\n"
            "This application is made as part of an academic project. "
            "By creating an account, you agree that the recommendations provided by NextChapter are generated by an AI model. "
            "The accuracy and relevance of recommendations may vary, and users should verify information before making decisions. "
            "We do not take responsibility for book content, availability, or accuracy of ratings."
        )
        disclaimer_accepted = st.checkbox("I have read and agree to the disclaimer.")
        col1, col2 = st.columns([1, 1])
        with col1:
            submit = st.form_submit_button("Create Account")
        with col2:
            login_now = st.form_submit_button("Login Now")
        
        if submit:
            if not disclaimer_accepted:
                st.error("You must accept the disclaimer to create an account.")
            else:
                response = requests.post(
                    'http://localhost:5000/signup',
                    json={'username': username, 'password': password}
                )
                if response.status_code == 201:
                    st.success("Account created! Please login.")
                    state.show_signup = False
                    state.show_login = True
                    st.rerun()
        
        if login_now:
            state.show_signup = False
            state.show_login = True
            st.rerun()

# Main content with recommendations
def main_content():
    if not state.get('session_id'):
        state.logged_in = False
        return

    verify_response = requests.post(
        'http://localhost:5000/verify',
        headers={'Session-ID': state.session_id}
    )
    
    if verify_response.status_code != 200:
        state.logged_in = False
        return
    
    state.username = verify_response.json().get('username', 'User')
    
    user_input = st.text_area("ğŸ’¬ Please enter your prompt: ", placeholder="e.g., I love mystery fantasy books ....", height=150)
    
    if st.button("Get Book Recommendations") and user_input:
        try:
            response = requests.post("http://127.0.0.1:5000/recommend", json={"input": user_input}, headers={"Session-ID": state.session_id})
            
            if response.status_code == 200:
                try:
                    response_data = response.json()
                    if response_data.get("recommendations"):
                        recs = response_data["recommendations"]
                        st.success("âœ… Book Recommendations Found!")
                        st.subheader("ğŸ“š Recommended Book:")
                        st.write(f"**{recs.get('book_itself', 'N/A')}**")
                        st.subheader("ğŸ“Œ Details:")
                        st.write(f"ğŸ“– **Category:** {recs.get('categories', 'Unknown')}")
                        st.write(f"â­ **Average Rating:** {recs.get('average_rating', 'N/A')}")
                        st.write(f"ğŸ—³ï¸ **Ratings Count:** {recs.get('ratings_count', 'N/A')}")
                        st.write(f"ğŸŒ **Language:** {recs.get('language', 'Unknown')}")
                        st.subheader("ğŸ”— Similar Books:")
                        st.write("ğŸ“Œ **By Title:**", ", ".join(recs.get("similar_title_books", ["No recommendations available"])))
                        st.write("ğŸ“Œ **By Description:**", ", ".join(recs.get("similar_description_books", ["No recommendations available"])))
                        st.write("ğŸ“Œ **By Category:**", ", ".join(recs.get("similar_category_books_faiss", ["No recommendations available"])))
                    else:
                        st.error(f"âŒ {response_data.get('message', 'Failed to get recommendations.')}")
                except Exception as e:
                    st.error(f"ğŸš¨ Error parsing the API response: {e}")
            else:
                st.error(f"âŒ API Request failed with status code {response.status_code}")
        except requests.exceptions.RequestException as e:
            st.error(f"ğŸš¨ API Error: {e}")
    else:
        st.info("Please enter a book preference or query and click 'Get Book Recommendations'.")

# Main app flow
def main():
    state.setdefault('logged_in', False)
    state.setdefault('show_login', False)
    state.setdefault('show_signup', False)
    state.setdefault('username', 'Guest')
    
    custom_header()
    
    if not state.logged_in:
        if state.show_login:
            login_form()
        elif state.show_signup:
            signup_form()
        else:
            st.info("ğŸ” Please login or sign up to access book recommendations")
        return
    
    main_content()

if __name__ == "__main__":
    main()